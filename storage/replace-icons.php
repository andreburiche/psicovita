function collectBladeFiles(string $dir, array $exclude): array
{
    $files = [];
    $iterator = new RecursiveIteratorIterator(new RecursiveDirectoryIterator($dir));
    foreach ($iterator as $file) {
        if (! $file->isFile() || $file->getExtension() !== 'php') {
            continue;
        }
        $path = str_replace('\\', '/', substr($file->getPathname(), strlen(dirname(__DIR__)) + 1));
        if (! str_ends_with($path, '.blade.php')) {
            continue;
        }
        if (str_contains($path, 'landing/partials/')) {
            continue;
        }
        if (in_array($path, $exclude, true)) {
            continue;
        }
        $files[] = $file->getPathname();
    }

    return $files;
}

function replaceSvgBlock(string $content, array $pathToIcon): array
{
    $count = 0;
    $pattern = '/<svg\b([^>]*)>(.*?)<\/svg>/s';
    $content = preg_replace_callback($pattern, function (array $m) use ($pathToIcon, &$count) {
        $inner = $m[2];
        $attrs = $m[1];

        // Skip if already processed or brand
        if (str_contains($inner, 'brand-psi')) {
            return $m[0];
        }

        // Extract primary path d
        if (! preg_match('/d="([^"]+)"/', $inner, $pathMatch)) {
            return $m[0];
        }
        $d = $pathMatch[1];

        $resolvedIcon = null;
        foreach ($pathToIcon as $fragment => $name) {
            if (str_contains($d, $fragment) || str_starts_with($d, $fragment) || str_contains($fragment, substr($d, 0, 40))) {
                $resolvedIcon = $name;
                break;
            }
        }

        // Match by first path in multi-path svgs
        if (! $resolvedIcon && preg_match_all('/d="([^"]+)"/', $inner, $allPaths)) {
            foreach ($allPaths[1] as $pathD) {
                foreach ($pathToIcon as $fragment => $name) {
                    if (str_contains($pathD, substr($fragment, 0, min(30, strlen($fragment))))) {
                        $resolvedIcon = $name;
                        break 2;
                    }
                }
            }
        }

        if (! $resolvedIcon) {
            return $m[0];
        }

        $class = '';
        if (preg_match('/class="([^"]*)"/', $attrs, $classMatch)) {
            $class = $classMatch[1];
        }

        $alpineAttrs = '';
        foreach (['x-show', 'x-cloak', ':class', 'x-bind:class'] as $alpine) {
            if (preg_match('/\s' . preg_quote($alpine, '/') . '="([^"]*)"/', $attrs, $am)) {
                $alpineAttrs .= ' ' . $alpine . '="' . $am[1] . '"';
            }
        }

        $count++;

        return '<x-ui.icon name="' . $resolvedIcon . '" class="' . $class . '"' . $alpineAttrs . ' />';
    }, $content);

    return [$content, $count];
}

